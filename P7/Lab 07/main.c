void Timer1ISR(void){
	timer++;
	fps++;
	if(timer >= 10){
		row_flag = TRUE;
		timer = 0;
	}
	if(fps >= 1250){
		next_frame = TRUE;
		fps = 0;
	}
}


void matrix_depict(uint8_t data[8]){
	uint8_t row_holder = 7; 
	if(row_flag){
		row_flag = FALSE;

		LPC_GPIO1 -> FIOCLR = (uint32_t) (0xffff<<16);
		uint8_t row = (uint8_t) (1<<row_holder);	
		LPC_GPIO1 -> FIOSET = (uint32_t) (reverse_byte(data[row_holder]) << 24);

		LPC_GPIO1 -> FIOSET = (uint32_t) (row<<16);
		row_holder--;
		if(row_holder == 0)
			row_holder = 7;
	}
}

